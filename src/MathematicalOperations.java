public class MathematicalOperations {
    public static void main(String[] args) {
        int x = 52;
        int y = 55;
        int result;

        //addition
        result = x + y;
        System.out.println("The addition is" + result);

        //substraction

        result = x - y;
        System.out.println("The result is" + result);

        //multiplication
        result = x * y;
        System.out.println("The result is" + result);

        //division
        result = x / y;
        System.out.println("The result is" + result);
    }
}
